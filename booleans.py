# Conditionals
if True:
  print("IF running...")
else:
  print("ELSE running...")


# Logic Operators: AND and OR 

# AND
if True and True:
  print("block will run...")

if True and False:
  print("block will NOT run...")

if False and False:
  print("block will NOT run...")

# OR
if True or True:
  print("block OR will run...")

if True or False:
  print("block OR will run...")

if False or False:
  print("block OR will NOT run...")